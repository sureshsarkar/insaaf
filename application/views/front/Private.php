      <p style="text-align: justify;"><span style="font-size: 3rem; color: #eb934f; font-weight: 600; font-family: none; text-align: justify;"><sub>A</sub></span>&nbsp; <b>private limited company</b> , or LTD, is a type of privately held small business entity. This type of business entity limits owner liability to their shares, limits the number of shareholders to 50, and restricts shareholders from publicly trading shares.
    </p>
     <h5><b>Basic requirements of Online Company Registration</b></h5>
     <p><b>There are a few requirements to be known before initiating a Private Limited Company in India:</b></p>
     <ul>
         <li style="text-align: justify;">The private limited company must have a unique name which should not be the same as any other registered company and trademark.</li>
         <li style="text-align: justify;">It is mandatory for a private limited company to have a minimum of two directors.</li>
         <li style="text-align: justify;">As well as, it is necessary to keep in mind that the private limited company should have a minimum of two shareholders.</li>
         <li style="text-align: justify;">All directors & members of a private limited company should have a digital signature certificate which will be used to register a private limited company.</li>
         <li style="text-align: justify;">There is no minimum capital required for initiating a private limited company.</li>
         <li style="text-align: justify;">The process of online company registration is quite simple; make sure that you have a unique name for your company which will surely help you with quick company registration.</li>
         <li style="text-align: justify;">You must avoid any offensive name for your private limited company registration.</li>
     </ul>
      
     <p style="text-align: justify;">A private company may be formed as per section 3 of the 2013 Act by two or more persons by giving their names and complying with the requirement of the registration process given under the provisions of the said Act. </p>
     <p style="text-align: justify;"><b>The following documents and information are required to be filled with the registrar within whose jurisdiction the registered office of the registering company is situated:</b></p>
     <ul>
         <li style="text-align: justify;">The memorandum and articles are duly signed by all the subscribers of the memorandum in the prescribed manner. </li>
         <li style="text-align: justify;">A declaration by the advocate, chartered accountant, cost accountant or company secretary in a prescribed manner who are engaged in the incorporation of the company and also by the persons named in the articles of the company as a director, manager or secretary of the company that all the requirements of Registration prescribed under the Act are complied with.</li>
         <li style="text-align: justify;">An affidavit from each subscriber to the MOA and the person named as the first director in the AOA of the company that he is not convicted for any offence related to the formation, management and promotion of the company and has never been guilty of fraud or a breach of the company.</li>
         
     </ul>
     <p><b>Detailed Registration Process</b></p>
         <p><b>The Ministry of Corporate Affairs governs the company registration process. The below registration process is basis the governing laws of MCA:</b></p>
         
         <ul>
             <li>Step 1: Application for DSC (Digital Signature Certificate)</li>
             <li>Step 2: Apply for DIN (Director Identification Number)</li>
             <li>Step 3: Application filing for Name availability</li>
             <li>Step 4: Drafting and Filing of eMoa and eAoA</li>
             <li>Step 5: Apply for PAN and TAN of the company</li>
             <li>Step 6: Certificate of Incorporation issued by RoC along with PAN and TAN</li>
             <li>Step 7: Open a Current Bank account on the company name</li>
         </ul>
         <p style="text-align: justify;"><b>Documents required for Private Limited Company Registration</b></p>
         <p style="text-align: justify;">In India, Private Limited Company registration cannot be done without proper identity and address proof. Prescribed documents will be needed for all the directors and the shareholders for the company incorporation. Listed below are the documents that are accepted by MCA for private Limited Company registration</p>
         <p><b>Identity & Address Proof:</b></p>
         <ul>
             <li>Copy of Pan Card of Directors (mandatory)</li>
             <li>Voter Id card or Driving License or Passport</li>
             <li style="text-align: justify;">Any one of: Bank Statement or Electricity Bill or Mobile Bill (not older than 2 months)</li>
             <li>Passport Size Photograph of Directors</li>
         </ul>
         <h5><b>Proof of Registered Office:</b></h5>
         <ul>
             <li style="text-align: justify;">Conveyance/ Lease deed/Rent Agreement etc. along with rent receipts (any one)</li>
             <li style="text-align: justify;">Copy of the utility bills (Telephone/Gas/Electricity bill) (not older than two months) (any one)</li>
             <li>NOC from Landlord</li>
         </ul>
