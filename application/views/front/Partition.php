      <p style="text-align: justify;"><span style="font-size: 3rem; color: #eb934f; font-weight: 600; font-family: none; text-align: justify;"><sub>P</sub></span>&nbsp;artition deed is that legal document which established that a joint property has now been divided among the joint owners and each of them have absolute claim on a certain share of the said property. Basically, in case of properties that are jointly owned by several members of a family, the division of the said property could be done by way of executing a partition deed.  </p>
      <p><b>When do you need a partition deed?</b></p>
      <p>Co-owners enjoy limited rights over a property, as their undivided shares leave a lot of scope for uncertainty. Even though all of these people are joint owners, they are not free to rent, sell or gift the property on their own accord, unless each party agrees to such a proposition. Basically, the consent of each co-owner is required to carry out any such transaction. <b> The need for a partition deed arises, when it becomes important to create a clear division of shares in the property.</b></p>
    
