     <p><b>When is a relinquishment deed created?</b></p>
     <p>An owner can transfer his right in a property through various legal instruments, including sale, or gift or will. So, when does the need to create a relinquishment deed arise and how is it different from other modes of property transfer?</p>
     <p>Transfer of rights in a property through a relinquishment deed is possible, only in cases of inherited properties. These would include all your ancestral properties, upon which you have a right by birth under the Hindu law and the self-acquired property of your father, in case he dies interstate.</p>
     <p><b>Who can create a relinquishment deed?</b></p>
     <p>Only co-owners of a property can relinquish their share in the property. Also, they can relinquish their share only in favour of another co-owner.</p>
