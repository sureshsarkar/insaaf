        <div id="CrPC4" class="collapse " aria-labelledby="headingOne" data-parent="#accordionExample">
            <div class="card-body text-justify textfontsize">
                (1) In this Code the following words and expressions have the following meanings unless a different
                intention appears from the subject or context:

                (a) 'Advocate-General'. 'Advocate-General' includes also a Government Advocate or where there is not
                Advocate-General or Government Advocate, such officer as the Provincial Government may, from time to
                time, appoint in this behalf;

                (b) 'Bailable offence', 'Non-bailable offence'. 'Bailable offence' means an offence shown as bailable in
                the second schedule, or which is made bailable by any other law for the time being in force; and
                'non-bailable offence' means any other offence:

                (c) 'Charge'. 'Charge' includes any head of charge when the charge contains more heads than one:
                (d) [Rep. by the Repealing and amending Act, 1923 (Act XI of 1923)}
                (e) [Omitted by Law Reforms Ordinance, 1972}.

                (f) 'Cognizable offence'. Cognizable case'. 'Cognizable offence' means an offence for, and 'cognizable
                case' means a case in, which a police officer, may, in accordance with the second schedule or under any
                law for the time being in force, arrest without warrant;

                (g) Omitted by A.O. 1949.

                (h) 'Complaint'. 'Complaint' means the allegation made orally or in writing to a Magistrate, with a view
                to his taking action under this Code, that some person whether known or unknown, has committed an
                offence, but it does not include the

                reports of a police-officer;

                (i) Omitted by Act II of 1950.

                (j) 'High Court'. 'High Court' means the highest Court of criminal appeal or revision for a Province:

                (k) 'Inquiry'. 'Inquiry' includes every inquiry other than a trial conducted under this Code by a
                Magistrate or Court;

                (l) 'Investigation'. 'Investigation' includes all the proceedings under this Code for the collection of
                evidence conducted by a police-officer or by any person (other than a Magistrate) who is authorized by a
                Magistrate in this behalf;

                (m) 'Judicial proceeding'. 'Judicial proceeding' includes any proceeding in the course of which evidence
                is or may be legally taken on oath;

                (n) 'Non-cognizable offence': 'Non-cognizable case'. 'Non-cognizable offence means an offence for, and
                'non-cognizable case' means a case in, which a police officer, may not arrest without warrant;

                (o) 'Offence', 'Offence' means any act or omission made punishable by any law for the time being in
                force; it also includes any act in respect of which a complaint may be made under section 20 of the
                Cattle Trespass Act, 1871.

                Offence- The word 'offence' as defined in the Code means any act or omissions made punishable by any law
                for the time being enforced apart those from under PPC. 1991 Cr.L.J. 1476 (Ind). An offence is
                constituted as soon as the act which constitute that offence have been committed. It remains an offence
                whether it is triable by a Court or not and the fact that the trial of the offence can only be taken up
                after certain specified conditions are fulfilled does not make it any the less an offence. AIR 1967 S.C.
                528. There is nothing wrong in law to regard a single act of firing at two persons as one offence. AIR
                1952 S.C. 45. However, an act or omission is an offence only if it is made punishable by any law for the
                time being in force 1931 P.C. 94.

                (p) 'Officer in charge of a police-station'. 'Officer in charge of a police-station' includes, when the
                officer in charge of the police-station is absent from the station-house or unable from illness or other
                cause to perform his duties, the police-officer present at the station-house who is next in rank to such
                officer and is above the rank of constable or, when the Provincial Government so directs, any other
                police-officer so present;

                (q) 'Place'. 'Place' includes also a house, building, tent and vessel:

                (r) 'Pleader'. 'Pleader', used with reference to any proceeding in any Court, means, a pleader or a
                mukhtar authorized under any law for the time being in force to practice in such Court, and includes (1)
                an advocate, a vakil and an attorney of a High Court so authorized, and (2) any other person appointed
                wit the permission of the Court to act in such proceeding;

                Pleader- With reference to any proceedings in the Court a pleader means a pleader or Mukhtar authorised
                under law for the time being in force and includes:
                (1) An advocate;
                (2) A vakeel or any attorney of High Court so authorised;

                (3) Any other person appointed with the permission of the Court. A co-accused can act as pleader if so
                permitted. AIR 1962 Pat 244. Private person must get the prior permission. 1991 P.Cr.L.J. 2425. The
                discretion of the Court in permitting person to appear as 'pleader' must be exercised judicially with
                due regard to the interest of the party engaging him. AIR 1978 S.C. 1019; 1978 Cr.L.J. 778.

                A power of attorney authenticated by fail authorities is a valid document. 1991 P.Cr.L.J. 25. A
                constituted attorney can appear for the accused. 1991 P.Cr.L.J. 2425.
                (s) 'Police-station'. 'Police-station' means any post or place declared generally or specially, by the
                Provincial Government to be a Police Station and includes any local area specified by the Provincial
                Government in this behalf:

                Police station-Police station is a place declared generally or specifically by the

                provincial Government to be a police station and includes any local area so specified except a
                beat-house. AIR 1960 Cal 519. Does not include vehicle. (1963) 3 SCR 386. Detention in a tee other than
                notified is illegal. PLD 1965 Lah 324.

                (t) 'Public Prosecutor'. Public Prosecutor' means any person appointed under section 492, and includes
                any person acting under the directions of a Public Prosecutor and any person conducting a prosecution on
                behalf of the State in any High Court in the exercise of its original criminal jurisdiction:

                Public prosecutor-Public Prosecutor means any person appointed u/s. 492 and includes Assistant Public
                Prosecutor and any person conducting prosecution under Public Prosecutor. PLD 1960 Dacca 783. He is
                bound to assist the Court with his fairly considered view and the Court is entitled to have the benefit
                of the fair exercise of his function. AIR 1957 S.C. 389. Government advocate under instructions of the
                Advocate General who is appointed u/s 492 to be Public Prosecutor for all cases in the High Court is a
                Public Prosecutor. 1966 Raj. Law Weekly 300; 1981 S.C. Cr. R. 301. Additional Government Advocate
                appointed as a public Prosecutor is a Public Prosecutor lawfully empowered to present appeal in the High
                Court against orders of acquittal. AIR 1971 S.C. 1977. A private pleader instructed by a private party
                will not be permitted to conduct prosecution. 1991 Mad. L.J. Cr. 624.

                (u) 'Sub-division'. 'Sub-division' means a sub-division of a district.

                [Clauses (v) and (w) of Subsection (1) of Section 4 omitted by Law Reform Ordi. 1972.]

                Omission of clauses (e) (v) & (w) by Law Reforms Ordinance 1972. item No. 1 enforced w.e.f. 26.12.1975
                vide No. Judl. 1-3(2)/75 dated 26.12.1975.

                (2) Words referring to acts. Words which refer to acts done, extend also to illegal omission; and

                Words to have same meaning as in Pakistan Penal Code. All words and expression used herein and defined
                in the Pakistan Penal Code, and not hereinbefore defined, shall be deemed to have the meanings
                respectively attributed to them by the Code.


