                offence, without the accused being committed to it for trial, upon a complaint in writing made by the
                Public Prosecutor.

                (2) Every such complaint shall set forth the facts which constitute the offence alleged, the nature of
                such offence and such other particulars as are reasonably sufficient to give notice to the accused of
                the offence alleged to have been committed by him.

                (3) No complaint under sub-section (1) shall be made by the Public Prosecutor except with the previous
                sanction.

                (a) in the case of the President or the Prime Minister or a Governor, or any Secretary to the Government
                authorized by him in this behalf.

                (b) in the case of a Federal Minister or Minister of State, Chief Minister or Provincial Minister, of
                any Secretary to the Government authorized in this behalf by the Government concerned.

                (c) in the case of any public servant employed in connection with the affairs of the Federation or of a
                province, of the Government concerned.

                (4) No Court of Sessions shall take cognizance of an offence under sub-section (1) unless the complaint
                is made within six months from the date on which the offence is alleged to have been committed.

                (5) When the Court of Session takes cognizance of an offence under sub-section (1), then,
                notwithstanding anything contained in the Code, the Court of Sessions shall try the case without the aid
                of a jury or assessors and in trying the case shall follow the procedure prescribed for the trial by
                Magistrate of warrant cases instituted otherwise than on a police report.

                (6) The provisions of this section shall be in addition to, and not in derogation of those of section
                198].


