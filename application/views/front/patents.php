                  <h3 style="margin-top:5px;"><b>Where to file a patent application?</b></h3>
                  <div class="row">
                     <div class="col-md-12 law_client no_margin">
                        <p style="text-align: justify;">It is filed electronically using Form-1 and provisional/ complete specification, with the prescribed fee at the appropriate patent office. The jurisdiction of a patent office is decided based on the following:</p>
                        <p style="text-align: justify;">Place of residence, domicile, or business of the applicant (first mentioned applicant in the case of joint applicants).</p>
                        <p>The place from where the invention originated.</p>
                        <p style="text-align: justify;">Address for service in India given by the applicant, when the applicant has no business or domicile place in India (Foreign applicants).</p>
                        <h5><b>Mumbai Patent Office Jurisdiction</b></h5>
                        <p style="text-align: justify;">The States of Gujarat, Maharashtra, Madhya Pradesh, Goa, Chhattisgarh, the Union Territories of Daman & Diu, and Dadra & Nagar Haveli fall under the Mumbai Patent Office jurisdiction.</p>
                        <h5><b>Delhi Patent Office Jurisdiction</b></h5>
                        <p style="text-align: justify;">The States of Haryana, Himachal Pradesh, Jammu and Kashmir, Punjab, Rajasthan, Uttar Pradesh, Uttarakhand, National Capital Territory of Delhi, and the Union Territory of Chandigarh fall under the Delhi Patent Office jurisdiction.</p>
                        <h5><b>Chennai Patent Office Jurisdiction</b></h5>
                        <p style="text-align: justify;">The States of Andhra Pradesh, Karnataka, Kerala, Tamil Nadu, and the Union Territories of Pondicherry and Lakshadweep fall under the Chennai Patent Office jurisdiction.</p>
                        <h5><b>Kolkata Patent Office Jurisdiction</b></h5>
                        <p style="text-align: justify;">The rest of India, the States of Bihar, Orissa, West Bengal, Sikkim, Assam, Meghalaya, Manipur, Tripura, Nagaland, Arunachal Pradesh, and Union Territory of Andaman and Nicobar Islands fall under the Kolkata Patent Office jurisdiction.</p>
