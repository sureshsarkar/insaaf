                  <h4 class="base_">Patents </h4>
                  <p  style="text-align: justify;"><span style="font-size: 3rem; color: #eb934f; font-weight: 600; font-family: none; text-align: justify;"><sub>P</sub></span>&nbsp;
                     atent registration can be obtained in India for an invention. A patent is a right granted to an individual or enterprise by the government that disallows others from making, using, selling, or importing the patented product or process without approval or consent.Patent filing is the primary step an inventor initiates to protect his or her invention from being misused. Patent filing in India is a tedious process, but it can be done quickly with proper guidance and support. An individual who wishes to secure a patent should get a consultation from expert patent practitioners.Before a patent registration is obtained, a rigorous check is done on whether the product is innovative or novel and industrially applicable. An individual can search the intellectual property regulator of India’s database to check if there is an object or invention that is the same or similar to the applicant’s invention.However, patent registrations are not applicable for all inventions, and the invention should satisfy specific criteria to obtain a patent in India.				.
                  </p>
                  <h3><b>Who can file a patent application?</b></h3>
               </div>
            </div>
            <div class="row">
               <div class="col-md-12 law_client no_margin">
                  <p style="text-align: justify;">A patent registration application for an invention can be made by any of the following people either alone or jointly with any other person:</p>
                  <p style="text-align: justify;">The true and first inventor</p>
                  <p style="text-align: justify;">True and first inventor’s assignee</p>
                  <p style="text-align: justify;">The representative of the deceased true and first inventor his / her assignee</p>
                  <p style="text-align: justify;">According to the Patent Act, a “person ” is any natural person, company, or association or body of individuals or government body, whether incorporated or not.</p>
                  <p style="text-align: justify;">In the case of a proprietorship firm, the application should be made in the proprietors’ name.</p>
                  <p style="text-align: justify;">In partnership firms, the names of all personally responsible partners must be included in the patent application.</p>
                  <p style="text-align: justify;">An assignee can also be a natural person or other than a legal person such as a registered company, an LLP, Section 8 Company, an educational institute, or government.</p>
                  <p style="text-align: justify;">The applicant is required to disclose the name, address, and nationality of the true and first inventor.</p>
               </div>
            </div>
            <div class="row">
               <div class="col-md-12 law_client">
                  <h3 style="margin-top:5px;"><b >What can be patented?</b></h3>
                  <div class="row">
                     <div class="col-md-12 law_client no_margin">
                        <p style="text-align: justify;">The Patent Act states that for an invention to be patentable, the invention must be a new product or process that involves an inventive step and can be used in the industry. For an invention to obtain patent registration, it should be technical and meet the following criteria-</p>
                        <p style="text-align: justify;">Novelty – The matter disclosed in the specification is not published in India or elsewhere before filing the patent application in India.</p>
                        <p style="text-align: justify;">Inventive Step – The invention is not apparent to a person skilled in the art in light of the prior publication/ knowledge/ document.</p>
                        <p style="text-align: justify;">Industrial applicability – Invention should possess utility so that it can be made or used in the industry.</p>
                     </div>
                  </div>
               </div>
            </div>
            <div class="row">
               <div class="col-md-12 law_client">
                  <h3 style="margin-top:5px; text-align:center;"><b >Types of patent application in India</b></h3>
                  <div class="row">
                     <div class="col-md-12 law_client no_margin">
                        <p style="text-align: justify;">Provisional application – Also known as a temporary application, a provisional application is filed when an invention is still in the pipeline and has not been finalized. Early filing of a patent will prevent any other related inventions from being designated as prior art to the inventor’s application.</p>
                        <p style="text-align: justify;">This type of patent application is filed when an invention requires additional time for development.</p>
                        <p style="text-align: justify;">Ordinary or Non-Provisional application – This type of application does not have any priority to claim or if the application is not filed in pursuance of any preceding convention application. A complete specification can be filed through:</p>
                        <p style="text-align: justify;">Direct Filing: Wherein complete specification is initially filed with the Indian Patent Office without filing any corresponding provisional specification.</p>
                        <p style="text-align: justify;">Subsequent Filing: Wherein complete specification is filed after the filing of the corresponding provisional specification and claiming priority from the filed provisional specification.</p>
                        <p style="text-align: justify;">Convention application – The convention application is filed for claiming a priority date based on the same or similar application filed in any of the convention countries.</p>
                        <p style="text-align: justify;">To avail a status of the convention, an applicant must apply to the Indian Patent Office within a year from the date of the initial filing of a similar application in the convention country.</p>
                        <p style="text-align: justify;">To re-iterate in simpler terms, a convention application entitles the applicant to claim priority in all the convention countries.</p>
                        <p style="text-align: justify;">PCT international application – Though the PCT international application does not provide an international patent grant, it paves the way for the streamlined patent application process in many countries at one go.</p>
                        <p style="text-align: justify;">The Patent Corporation Treaty governs it and can be validated in up to 142 countries. Filing this application will protect an invention from being replicated in these countries.</p>
                        <p style="text-align: justify;">PCT National application – This application is filed if the applicant discovers that he has come across an invention, a slight modification of the invention that has already been applied for or patented by the applicant. PCT National application can be filed if the invention does not involve a substantial inventive step.</p>
                        <p style="text-align: justify;">Divisional application – An applicant may choose to divide an application and furnish two or more applications if a particular application claims more than one invention. The priority date for these applications is similar to that of the parent application.</p>
                     </div>
                  </div>
               </div>
            </div>
