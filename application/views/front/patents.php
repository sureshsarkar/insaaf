                  <h3 style="margin-top:5px;"><b>What are the benefits of filing a patent in India?</b></h3>
                  <div class="row">
                     <div class="col-md-12 law_client no_margin">
                        <h5><b>Here are the benefits of filing a patent registration in India:</b></h5>
                        <p style="text-align: justify;">A patent is a form of encouragement for innovations and inventions. Once the applicant is granted the patent, they become the owner of the invention or the idea.</p>
                        <p style="text-align: justify;">Patent registration is essential for a business as the patent restricts the competitors from copying, selling, or importing the intellectual property without permission. This way, the patent holder can protect his patent rights in support of the existing laws.</p>
                        <p>Patents can be sold as well as licensed like other forms of property.</p>
                        <p>The inventor can also transfer it.</p>
                        <p style="text-align: justify;">A patented product improves the brand perception and potentially enables the business to charge a premium.</p>
                        <p style="text-align: justify;">With exclusive patent rights, the patent owner controls the use of the invention for twenty years and longer.</p>
