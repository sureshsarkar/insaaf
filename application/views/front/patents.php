                  <h3 style="margin-top:5px; text-align:center;"><b >How to File Patent application?</b></h3>
                  <div class="row">
                     <div class="col-md-12 law_client no_margin">
                        <ol>
                           <li>
                              <h5><b>Patent Search</b></h5>
                           </li>
                           <p style="text-align: justify;">To file a successful patent registration, you need to ensure that your invention idea is unique. Carrying a patent search will confirm this, and the individual can also avoid lengthy procedures.</p>
                           <br>
                           <li>
                              <h5><b>Patent application filing</b></h5>
                           </li>
                           <p style="text-align: justify;">Patent Filing is considered to be the most crucial aspect. The complete process specification is a specialized task that can be done correctly through expert advice.</p>
                           <br>
                           <p style="text-align: justify;">Drafting a patent application is an art, and it will be wise to choose to seek expert help. If the individual is in the initial stages of research and development, it is best to file a provisional patent application.</p>
                           <li>
                              <h5><b>Preparing patentability report</b></h5>
                           </li>
                           <p style="text-align: justify;">The patent professionals or agents will then do extensive research and prepare the patentability report. Hence, the applicant should attach all the specified documents along with the patent application.</p>
                           <li>
                              <h5><b>Publication of patent application</b></h5>
                           </li>
                           <p style="text-align: justify;">After this, the application is then published in the Patent journal within 18 months. Request for early filing of the patent can be made along with the prescribed fees.</p>
                           <p style="text-align: justify;">Publication of Filed Patent</p>
                           <p style="text-align: justify;">For patent registration in India, the Indian patent office keeps every patent application confidential until it gets officially published in the Patent Journal.</p>
                           <p style="text-align: justify;">The publication of filed patent happens automatically after 18 months from filing the applications, and there is no need for any request to be raised.</p>
                           <li>
                              <h5><b>Patent Examination</b></h5>
                           </li>
                           <p style="text-align: justify;">Within 48 months from the patent’s first filing, there would be a formal submission of a request for the patent examination. Suppose the applicant fails to file within the specific time, the application will be treated as withdrawn by the patent office. The examiner then conducts a comprehensive investigation and releases the first examination report called patent prosecution.</p>
                           <li>
                              <h5><b>Patent objections</b></h5>
                           </li>
                           <p style="text-align: justify;">Patent applications also receive objections, so it is mandatory to analyze the patent examination report and draft a proper response to the objections.</p>
                           <li>
                              <h5><b>Grant of patent</b></h5>
                           </li>
                           <p style="text-align: justify;">Once all the patentability requirements are met, the grant patent’s notification will be published in the Patent Journal.</p>
                        </ol>
