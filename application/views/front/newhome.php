<script>
$("#formSubmit").submit(function(e) {
    e.preventDefault();
    // check captcha
    var name = $(".name").val();
    var email = $(".email").val();
    var mobile = $(".mobile").val();
    var query = $(".query").val();
    var gender = document.getElementsByName('gender');
    var genderStatus = false;
    if (gender[0].checked || gender[1].checked || gender[2].checked) {
        genderStatus = true;
    }

    if (mobile == "" || mobile.length < 10) {
        alert("Please enter valid mobile number");
        return false;
    }
    let genderVal = 0; //(gender[0].checked)?1:2;

    if (gender[0].checked) {
        genderVal = 1;
    } else if (gender[1].checked) {
        genderVal = 2;
    } else if (gender[2].checked) {
        genderVal = 3;
    }

    sessionStorage.setItem("user_query", query);

    var url = "<?=base_url()?>index/homeRegister";
    $.ajax({
        type: "POST",
        url: url,
        data: {
            name: name,
            query: query,
            email: email,
            mobile: mobile,
            gender: genderVal
        },

        success: function(responce) {
            var res = JSON.parse(responce)
            if (res.status == 3) {
                window.location.href = res.redirect;
                return false;
            } else {
                $(".formCon").removeClass('hidden');
                $(".progressCon").addClass('hidden');
                $(".successCon").addClass('hidden');
                return false;
            }
        }
    });
    return false;
});
</script>

