Diamond Grading Report!
Each diamond is graded on the basis of 4C's i.e. Clarity, Cut, Color and Carat-weight. At Jaipur Gems LaboratoryÂ®, each Diamond is graded carefully by experienced Diamonds .This report not only covers 4C's but also indicates if the diamond is treated or enhanced in any way to its present state. GemsLaboratory follows the widely accepted GIA - Grading system for grading of Diamonds.

Gemstone Identification Report!
Gemstones are identified on the basis of their Physical & Optical properties This Report mentions the properties of the Gemstone viz weight ,Shape & cut,measu-rement etc. Each Gemstone is examined very carefully by our professional Gemologists using internationally accepted Procedures and Tools. Each Report has the photograph of the stone mentioned in the Report for Identification.
Jewellery Authentication Report!
This report is meant for Jewellery items studded with Diamonds & Gemstones. This report mentions all important information related to Jewellery Items that is required by the supplier as well as by the buyer. Each jewellery item is examined by expert gemologists, under strict regulations and using specialized equipment.

Colour Gemstone Authenticity Report!
Color Gemstones are identified on the basis of their properties This Report mentions the name of the Gemstone along with properties of the Gemstone like weight ,Shape & cut. Each Report has the photograph of the Gemstone mentioned in the Report for Identification. A unique certification ID would be provided which can be verified from our website.








